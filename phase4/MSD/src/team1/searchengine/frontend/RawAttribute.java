package team1.searchengine.frontend;

/**
 * Attributes of a RawRecord
 * 
 * @author hrivks
 *
 */
public class RawAttribute {
	public String key;
	public String value;

	public RawAttribute() {
		// default constructor
	}

	public RawAttribute(String key, String value) {
		this.key = key;
		this.value = value;
	}

}
